package com.java_exceptions;

public class Facultity  {
        String nameFaculty;


    public Facultity(String nameFaculty) {
        this.nameFaculty = nameFaculty;
    }

    @Override
    public String toString() {
        return "Facultity{" +
                "nameFaculty='" + nameFaculty + '\'' +
                '}';
    }
}

